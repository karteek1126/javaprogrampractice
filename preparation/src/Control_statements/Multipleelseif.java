package Control_statements;

public class Multipleelseif {

	public static void main(String[] args) {
		 
		int A = 100;
		
		if(A < 20) {
			
			System.out.println(A + "  lesser then 20");
		}
		
		else if (A < 40) {
			
			System.out.println(A + "  leasser then 40");
		}
		
		else if (A < 50) {
			
			System.out.println(A + "  leasser then 50");
		}
		
		else if (A < 110) {
			
			System.out.println(A +  " is Leasser then 110");
		}
	}

}
